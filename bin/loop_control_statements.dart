void main() {
  // For loop

  for (var i = 0; i < 4; i++) {
    print("Hello");
  }

  // while loop
  var i = 0;

  while (i < 4) {
    print("Hello");
    i++;
  }

  // do-while loop
  do {
    print("Hello");
    i++;
  } while (i < 4);

  // Loops can be categorised into definite and indefinite. Definite is FOR LOOP
  // Examples of indefinite loop include WHILE and DO WHILE LOOPS
}
